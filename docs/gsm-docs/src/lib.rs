//! # Game Server Management
//!
//! Welcome to the Game Server Management (GSM) workspace documentation.
//!
//! This documentation provides details on all the crates within the GSM workspace.
//!
//! ## Important Note on Installation
//!
//! This project is structured as a Cargo workspace, which means it consists of multiple interdependent crates. These crates are designed to work together and share common dependencies.
//!
//! To ensure that all local path dependencies are correctly resolved, you **must** clone the repository and build the project from the source.
//!
//! **Installation via `cargo install` is not supported and will not work.**
//!
//! Please refer to the [README.md](https://github.com/mbround18/game-server-management/blob/main/README.md) for instructions on how to build the project.
